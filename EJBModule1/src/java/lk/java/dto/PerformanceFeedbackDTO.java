package lk.java.dto;

/**
 *
 * @author Rajitha Yasasri
 */
public class PerformanceFeedbackDTO {
    private int id;
    private String description;
    private String personGiven;
    private int employeeId;
    private String employeeName;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the personGiven
     */
    public String getPersonGiven() {
        return personGiven;
    }

    /**
     * @param personGiven the personGiven to set
     */
    public void setPersonGiven(String personGiven) {
        this.personGiven = personGiven;
    }

    /**
     * @return the employeeId
     */
    public int getEmployeeId() {
        return employeeId;
    }

    /**
     * @param employeeId the employeeId to set
     */
    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    /**
     * @return the employeeName
     */
    public String getEmployeeName() {
        return employeeName;
    }

    /**
     * @param employeeName the employeeName to set
     */
    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }
}
